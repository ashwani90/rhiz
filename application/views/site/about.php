              <p>
                  Architecture is not really a domain of ‘itself’ but ‘reflections’. It signifies or is vehicle of meaning for knowledge beyond itself and in our approach architecture is the rhizome of techno-socio-philosophical ideas. In Deleuze & Guattari’s view rhizome is characterized by “ceaselessly established connections between semiotic chains, organizations of power, and circumstances relative to the arts, sciences, and social struggles”. Rhizome has no beginning or end; it is always in the middle, between things, interbeing, intermezzo. The rhizome is reducible to neither the one or the multiple. It is always in middle (milieu) from which it grows and which it overspills.”
              </p>
