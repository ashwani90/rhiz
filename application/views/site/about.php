              <p>
                  Rhizome is a continuously growing horizontal underground stem structured in complex interconnections, which lead to a rich concentration of qualities in the plant. Deleuze & Guattari explored this idea in contrast to the Arborescent structure of knowledge. A model which is not reclusive but inclusive in nature and borrows knowledge from seemingly different domains. This gives the possibility of percolation and connections from various knowledge systems and delivers a new whole which is richer and more fascinating.
